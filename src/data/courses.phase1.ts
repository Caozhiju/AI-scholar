第三步，将每篇作文的偏误记录在表格中，标注出偏误类型和具体用词。

第四步，根据学生的汉语水平（初级、中级、高级）对数据进行分组。

第五步，统计各组偏误的频次，制作对比表格。

第六步，根据数据撰写分析报告。

整个过程，即使一天分析20篇，也需要50个工作日。这还不包括数据处理和报告撰写的时间。


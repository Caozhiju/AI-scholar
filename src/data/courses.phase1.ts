第三步，运行脚本。计算机开始自动处理所有作文。这个过程通常只需要几分钟。

第四步，脚本自动输出分析报告，包括：总偏误频次统计表；各水平组偏误类型分布对比；高频偏误词列表；各篇作文的偏误明细。

